then
   for fichier in ${R_figlist}
   do
      ext=`echo ${fichier##*.}`
      path=`dirname ${fichier}`
      base=`basename ${fichier}`
      prefix=`basename ${base} .${ext}`
      text="${path}/${prefix}.txt"
