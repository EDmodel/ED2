!==========================================================================================!
!==========================================================================================!
!     Function that checks whether a number is NaN.  This works with PGI, Intel, and GNU.  !
!------------------------------------------------------------------------------------------!
logical function isnan_real(x)
   implicit none
   !------ Arguments. ---------------------------------------------------------------------!
   real, intent(in) :: x
   !---------------------------------------------------------------------------------------!

#if defined(PGI)
   isnan_real = x /= x
#else
   isnan_real = isnan(x)
#endif

   return
end function isnan_real
!==========================================================================================!
!==========================================================================================!





