!==========================================================================================!
!==========================================================================================!
!     Routine returns CPU time.  Called with ICALL=1 at beginning of timestep, ICALL=2 at  !
! end of timestep.                                                                         !
!------------------------------------------------------------------------------------------!
subroutine timing(icall,t1)
   implicit none
   integer, intent(in)   :: icall
   real   , intent(out)  :: t1
   real   , dimension(2) :: et(2)

#if defined(GNU)
   real                  :: etime
#else
   real   ,external      :: etime
#endif

   select case (icall)
   !----- Start call. ---------------------------------------------------------------------!
   case (1)
      T1=ETIME(et)
   !----- End call. -----------------------------------------------------------------------!
   case (2)
      T1=ETIME(et)
   end select

   return
end subroutine timing
