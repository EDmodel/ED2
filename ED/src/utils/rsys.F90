!==========================================================================================!
!==========================================================================================!
!     Routine to get command line argument.                                                !
!------------------------------------------------------------------------------------------!
subroutine ugetarg(i,arg)
   implicit none
   integer          :: i
   character(len=*) :: arg
       

   call getarg(i,arg)

   return
end subroutine ugetarg
