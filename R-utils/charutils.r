#==========================================================================================#
#       This function is similar to Fortran adjust right.                                  #
#------------------------------------------------------------------------------------------#
adjust.right <<- function(x,width){
   if (is.null(width)) width      = max(nchar(x))
   fmt        = paste0("%",width,"s")
   out        = sprintf(fmt,x)
   names(out) = names(x)
   return(out)
}#end adjust.right
