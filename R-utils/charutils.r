#==========================================================================================#
#       This function is similar to Fortran adjust left.                                   #
#------------------------------------------------------------------------------------------#
adjust.left <<- function(x,width=NULL){
   if (is.null(width)) width   = max(nchar(x))
   names.x = names(x)

   out        = sapply( X   = x
                      , FUN = function(x,w){
                                 ans                     = rep(" ",times=w)
                                 ans[sequence(nchar(x))] = unlist(strsplit(x=x,split=""))
                                 ans                     = paste(ans,collapse="")
                                 return(ans)
                              }#end FUN
                      , w   = width
                      )#end sapply
   names(out) = names.x
   return(out)
}#end adjust.left
